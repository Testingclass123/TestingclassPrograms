package javaprogram;

public class Unaryoperator {

	public static void main(String[] args) {
		//Increment
		       //1.Preincrement
		      //2.PostIncrement
		//Decrement
		//1.Predecrement
		//2.Postdecrement

		int a=1;
		System.out.println(++a);
		
		int b=1;
		System.out.println(b++);//
		System.out.println(b);
		
	}

}
